    private void jButtonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegisterActionPerformed
        // TODO add your handling code here:
        String fname= jTextFieldFirstName.getText();
        String lname= jTextFieldLastName.getText();
        String uname= jTextFieldUsername.getText();
        String pass= String.valueOf(jPasswordField.getPassword());
        String retype_pass= String.valueOf(jPasswordFieldReType.getPassword());
        
        SimpleDateFormat dateformat= new SimpleDateFormat("yyyy-MM-dd");
        String bdate= dateformat.format(jDateChooser.getDate());        
        String address= jTextAreaAddress.getText();
        
        if (fname.isEmpty() || lname.isEmpty() || uname.isEmpty()
                || pass.isEmpty() || retype_pass.isEmpty() || jDateChooser.getDate()== null || address.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please complete all fields.");
        }
        else if(!pass.equals(retype_pass)){
            JOptionPane.showMessageDialog(null, "Passwords don't match. Re-type the password again");
        }
        else{
            //Start login process
            userRegister(fname, lname, uname, pass, bdate, address);
        }
    }//GEN-LAST:event_jButtonRegisterActionPerformed

