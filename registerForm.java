    private void userRegister(String fname, String lname, String uname, String pass, String bdate, String address) {
        Connection dbconn= myConnection.connectDB();
        if(dbconn != null){
        try {
            PreparedStatement st= (PreparedStatement)
                    dbconn.prepareStatement("INSERT INTO users (firstname,lastname,username,password,bday,address) VALUES(?,?,?,?,?,?)");
            
            st.setString(1, fname);
            st.setString(2, lname);
            st.setString(3, uname);
            st.setString(4, pass);
            st.setString(5, bdate);
            st.setString(6, address);
            
            int res= st.executeUpdate();
            JOptionPane.showMessageDialog(this, "User data added.", "Success", JOptionPane.INFORMATION_MESSAGE);

            jTextFieldFirstName.setText("");
            jTextFieldLastName.setText("");
            jTextFieldUsername.setText("");
            jPasswordField.setText("");
            
            jTextAreaAddress.setText("");
            
            
        } catch (SQLException ex) {
            Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
        }
        }else{
            System.out.println("The connection is not available");
        }
    }
